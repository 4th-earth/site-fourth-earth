<p>“Ah, yes, humans,” Url (ūrl) began. “Humans were a magnificent species.” His eyes widened as he scanned the classroom.</p>

<p>Referring to the space the nine of them occupied as a <em>classroom</em> felt odd to Url. First, it wasn&#8217;t a room; it was a clearing in the forest at the heart of The Cradle. Second, The Cradle’s Hub was neither strictly nor primarily for classes; therefore, it still wouldn’t qualify as a classroom.</p>

<p>Url tilted his head to the canopy of the forest. The weight of his Citizen Goggles added a welcome pressure to his eye sockets. He slowly brought his head back to see the class members.</p>

<p>There were eight learners today. Url preferred smaller classes, as they made conversation flow and interactions easier. Four students he recognized, and four he did not. They sat on the ground in a circle. If Url spread his arms, and the students to his left and right did the same, they would barely touch fingertips.</p>

<p>Granted, the goggles each of them wore rendered their physical limitations moot. They didn’t even need to occupy the same space to interact with a projection, because it existed in the augmented reality provided by their goggles.</p>

<p>A notification appeared in Url&#8217;s Citizen Goggles letting him know he had received eight credits from the Citizen Credit Pool for starting the lesson, one credit for each student present. A flick of his eyes dismissed it and disabled future notifications.</p>

<p>He installed this custom gesture in his youth because he didn’t want the distraction of notifications and didn’t want token reimbursement clouding the presentation of facts as he knew them.</p>

<p>“Human is the name they gave their species. However, we commonly refer to them as First Earthers. To the best of their, and our, knowledge, they were the first higher-order species on this planet. Can someone tell me a reason we refer to them as a higher-order species?”</p>

<p>Lafit’s (luh-feet) cat-like, tapered ears atop her head twitched and the vertical slits of her pupils widened as she started making gestures with her hands.</p>

<p><em>She’s testing her speed again</em>, Url thought.</p>

<p>In what felt like no time at all, a message scrolled across each student&#8217;s visual field, and they could hear a lifelike, synthetic voice projected from a speaker running the width of Lafit’s goggles. The message read:</p>

<p><strong>Lafit:</strong> <em>When compared to other animal species on the planet, First Earthers could manipulate natural resources to their benefit in a way other animals had not achieved in the same period. The interplay of other characteristics made this manipulation possible, again, at a depth not seen in other animal species.</em></p>

<p>“Thank you, Lafit. That is correct.”</p>

<p>A notification appeared in Lafit&#8217;s Citizen Goggles informing her she had earned three credits for answering correctly. Had Url and the Credit System determined the answer was incorrect, she would have received one credit for the attempt in the classroom context.</p>

<p>Url continued, “In fact, First Earthers are the ones who named this planet Earth. This name is one of many things we use to this day. From First Earth to Fourth Earth, the name survives, even though First Earthers do not.</p>

<p>“There are many theories why humans went extinct; however, it’s easier to agree on what did <em>not</em> happen. Therefore, we’ll begin by listing what we know did not cause their extinction.</p>

<p>“This will be a divergent, collaborative effort,” Url added.</p>

<p>Url made a gesture with his hand as if putting his finger in a cup to stir its contents, and a small rectangle appeared in each student’s lower visual field.</p>

<p>“We’ll set a timer for three minutes.”</p>

<p>As he said the words, a timer appeared in the top portion of each student&#8217;s visual field. “Remember, this is what you think we know, beyond a reasonable doubt, did <em>not</em> cause the extinction of First Earthers. If you run out of ideas before the timer ends, mark yourself done. Go.”</p>

<p>The timer started its countdown.</p>

<p>Url looked around the circle. </p>

<p>The diversity of minds and how they physically presented in the world fascinated him for as long as he could remember, even in a collective this small.</p>

<p>As the facilitator of this session, Url could see a count of individual items being created by the students; therefore, using not-so-complicated math, he knew each student was creating items. However, some students barely moved, others almost danced, and others moved their mouths as if they were singing without making a sound.</p>

<p>Lafit, for example, had permanently lost her ability to speak, and she seemed to compensate by talking with her hands. A naive onlooker might think the gestures were random, but Url had known her long enough to know she favored similar gestures for most words and ideas. </p>

<p>While the Citizen Goggles made standard sign languages unnecessary for Fourth Earthers, he knew First Earthers had well-established signing.</p>

<p>Dea (day-uh), for his part, slipped into a meditative stillness from the hare-like ears to his hands cupped in his lap. Only an observant onlooker would notice the micro-expressions beyond the rim of his goggles.</p>

<p>Deci (deh-see) appeared to talk to themselves, and the Citizen Goggles almost disappeared against their ebony skin. If it weren&#8217;t for the reflection and highlights of the lens material, one would not be criticized for thinking they weren’t wearing goggles at all. Granted, not wearing goggles in The Cradle means they’d lose credits by the hour and not be able to take part in this classroom setting.</p>

<p>Url preferred larger, more encompassing goggles, in part because he believed the added weight and pressure helped him focus, and in part because he could perform complex actions undetected. This level of privacy wasn’t available to many early, mid, and late First Earthers, given the tools of their time.</p>

<p>A soft chime sounded in Url&#8217;s ear, indicating a student had marked themselves done. For this exercise, he disabled any indicator of who finished, but a counter displayed in his periphery. The chime sounded a second time. Followed by a third. No learner knew whether someone had finished, was still working, or was doing something else.</p>

<p>Url glanced at the timer without moving his body.</p>

<p>“Two minutes,” Url said softly to the group.</p>

<p>With over one minute remaining, all the students had marked themselves done.</p>

<p>“Everyone has marked themselves done. I’ll give you a moment to change your mind.” Url paused and looked around the circle. “Going once.” He exhaled. “Going twice.” He inhaled. “Done.” He let out a longer exhale. The timer disappeared from everyone’s visual field.</p>

<p>Url made a gesture with his hand, and the cards appeared to be pulled from each student’s Citizen Goggles into the center of the ring.</p>

<p>From Url’s perspective, the small rectangles floated in a rotating, sphere-like structure. Lafit preferred an arrangement with her inputs in a column to her left, and everyone else’s inputs in a grid occupying most of the remaining space. Dea did something similar, but the rectangles stacked, making one visible and the rest appearing to go back in space, instead of a grid holding his peers’ inputs. Deci lined their inputs similar to Lafit and Dea, and created groupings of similar themes from their peers using subtle hand gestures.</p>

<p>“We&#8217;ll take a few moments to look at what we’ve created. Look at your inputs and those of your peers. If you believe you can merge one or more of your inputs with one or more of your peers’ inputs, mark yourself with a circle. We have a lot, so we&#8217;ll set a timer for ten minutes. Mark yourself done when you’ve gone through all the inputs.”</p>

<p>A question mark floated above Deci’s head before fading away.</p>

<p>“Yes, Deci?”</p>

<p>“We&#8217;re only comparing our inputs to the others?”</p>

<p>“Correct. If you think you had the same idea as one or more of your peers, that’s a potential point of convergence. By extension, you will also identify similarities among your peers, but that is not the focus. Therefore, if you see similarities among the inputs of your peers, but they don’t relate to one of your own, consider it, but do not fixate on it.”</p>

<p>Deci nodded.</p>

<p>“Ready?” Url asked the group at large.</p>

<p>Some nodded. Others took a deep breath.</p>

<p>“Go.”</p>

<p>Lafit rapidly reviewed her column of inputs from top to bottom to reacquaint herself. She glanced in a semi-focused way at the grid holding the inputs of her peers. Each card in the grid and its content were readable. Her eyes randomly drifted over the cards. When she noticed an input similar to one in her column, she gestured, and the card floated next to one she created. Any card she couldn’t match with one of hers remained in the grid. When she finished, she made a gesture to clean up and realign the grid.</p>

<p>Url noticed a circle above Lafit’s head, and that she had not marked herself done.</p>

<p>Dea preferred one card at a time. The first read, “Human effects on climate made the Earth uninhabitable for them.” This was like the card he created that said, “Climate change,” and he slid the card behind his. The next card read, “They left Earth.” This was not similar to any of his inputs, and he slid it with his eyes to his right, starting a stack of unique inputs compared to his own.</p>

<p>Deci colored their inputs teal with a narrow outline. They maintained the groupings they created of the peer inputs. The first of their inputs read, “Human effects on climate made the Earth uninhabitable for them.” They enlarged their input and slid it to the climate-themed grouping, ensuring it appeared in front of the group. While they appreciated the input that read, “Climate change,” they thought their articulation was more complete. However, they determined the input reading, “They left Earth” was more concise and just as clear as the input they created reading, “They mastered extraterrestrial travel, exploration, and colonization,” which was similar to, but relied on less conjecture than another input with a similar theme reading, “Extraterrestrial beings visited Earth, and First Earthers went with them.” Deci added their input to the group, made the “They left Earth” input yellow with a wide border, and placed it visually in front of and larger than the cards in the group.</p>

<p>A soft chime sounded in Url&#8217;s ear, indicating a student believed they were done. He glanced around the circle and noticed a solid line around Lafit. Url set his classroom sessions in a way to minimize, as much as possible, feelings of superiority or inferiority amongst students, especially during individual tasks common to the collective. None of the other learners would have seen this line around Lafit or the circle above her head. </p>

<p>Url thought back to a conversation between them, where Lafit explained that, since losing her voice, she was compelled to compensate by optimizing as much as she could. Url identified it as a need to compete. Lafit disagreed because competition implies she was optimizing to oppose someone else, while she viewed it as improving herself regardless of others. The conversation stalled, and remained stalled, when Url asked, “If you&#8217;re doing it to oppose a previous version of yourself, someone without those skills and abilities, how is that not competition?”</p>

<p>Another chime; this time it was Deci. Followed shortly by Dea.
With three minutes remaining on the timer, a solid line outlined each student, and most had a circle over their head.</p>

<p>“You’ve all marked yourselves done. I’ll give you a moment to verify with yourself.”</p>

<p>Url repeated the ritual of a verbal count and breathing. He believed it helped center the class and sync the rhythm of the individual students.</p>

<p>“If you are prepared to share your findings, fill your circle.”</p>

<p>Almost before he finished speaking, the circle above Lafit filled, and had the number one inside. It was followed immediately by Deci’s circle being filled with the number two. No one else filled their circles.</p>

<p>The addition of the numbers was another modification that Url had installed. Even though Lafit was first, Url said, “Deci, please begin.”</p>

<p>The personal card arrangements every other student had made shrank and slid to the periphery of their visual field.
Deci smiled and nodded. “Thank you.”</p>

<p>Deci stretched their arm in front of them and outlined all but one of their groupings. Once completed, Deci opened their hand, palm forward, and made a gesture minimizing the outlined inputs and moving them to the side of their visual field.</p>

<p>Deci moved their hand over the remaining group and pulled it to the center of their visual field. They pushed forward as if to thrust the group into the center of the round table of students.
This single grouping appeared in the center of the visual field of the other students. </p>

<p>For Lafit, the group appeared one item at a time, and was arranged with a yellow card at the top, a teal card below that, and a grid of a few other cards below that. </p>

<p>For Dea, the group appeared to fly from Deci to him in three layers: yellow card, teal card behind it, and a less organized group behind that. He leaned sideways a bit to reveal all three layers in a parallax effect.</p>

<p>“The yellow card with the thick border captures the essence of the concept well; I did not write it. The teal card with the thin border is the one I created.</p>

<p>“I consent and suggest merging the one I created into the yellow. I would also recommend merging the other undecorated inputs into the yellow one as well, but that is not my consent to give.”</p>

<p>Url nodded. “Everyone, look at the cards involved. If you don’t consent to this change, please say so, especially if the card to be merged is one you created.”</p>

<p>Url started scanning the circle. “Going once. Going twice. Done.” Url turned to Deci, “Make it so.”</p>

<p>Deci nodded and the cards merged, leaving the card reading “They left Earth.” The card slid to the top of each student’s visual field. The merged cards also disappeared from their minimized arrangements.</p>

<p>The circle above Dea’s head filled; the number two was inside.</p>

<p>“Dea, you’re next,” Url said.</p>

<p>Lafit shifted her butt on the ground, her tail twitching slightly before resuming its neutral curl and sway.</p>

<p>Dea nodded, “Thank you.”</p>

<p>With no visible movement whatsoever and following the format established by Deci, Dea quickly pulled the card he thought communicated the concept well, separated it from the one he created, and grouped the remaining cards. With no sign from him to prepare the others, each student’s visual field became occupied by the cards.</p>

<p>The first card presented to Lafit read, “Human effects on climate made the Earth uninhabitable for them.” The one below it appeared, reading “Climate change.” With a few others appearing in a grid below that.</p>

<p>Dea knew that, no matter his personal settings, each student could identify a primary card, a secondary card, and a tertiary group of cards. He said, “I did not write the first, but I think it represents the concept more clearly than mine, which is the second. I believe the others can be merged into this one. I consent to these changes.”</p>

<p>Url asked, “Does anyone not consent to this change?”</p>

<p>Lafit’s voice and an accompanying message said:</p>

<p><strong>Lafit:</strong> <em>I do not consent.</em></p>

<p>“Explain,” Url requested.</p>

<p>After losing her voice, Lafit searched for a voice for herself. She considered using her original voice, compiled from her recorded speech, but decided against it as she didn’t appreciate her voice. The baseline voice she chose was low enough to sound more adult, but high enough to cause the hairs on her neck to tingle with certain words. She altered the base voice to add a slight rasp.</p>

<p>Lafit’s chosen voice resonated from the projection speaker in her goggles.</p>

<p>“We don’t know humans impacted the climate enough to effect drastic change. Even humans didn’t come to full agreement before their extinction. Therefore, I think ‘Climate change’ works just as well. Whether humans caused the climate to change is irrelevant. Therefore, I propose merging the cards into that one.”</p>

<p>Deci spoke, “I think it’s a question of historical relativism and the search for causality.”</p>

<p>Url tilted his head to the side and closed his eyes. “Continue.”</p>

<p>“To the best of our knowledge, First Earthers understood, as we do, that climate change happens. Therefore, as an articulation, ‘Climate change’ is simply a statement of reality on this planet. Historically, though, First Earthers weren’t debating that point; they were debating the causes. Specifically, what contribution did they themselves have in causing changes to the climate that would be long-lasting, detrimental to their wellbeing, and so rapid that they could not adapt to them as a species?</p>

<p>“If we merge these other articulations into ‘Climate change,’ we could lose the historical relevance to First Earthers and why it was once believed to have caused their extinction,” Deci finished.</p>

<p>“Lafit,” Url started, “you opened this dissent. Would you like to continue or yield the point?”</p>

<p>A message and Lafit’s projected voice said:</p>

<p><strong>Lafit:</strong> <em>I yield.</em></p>

<p>“Would anyone else like to offer a dissenting perspective on this merge?”</p>

<p>A thinking icon appeared and floated upward from Daxus’s (dak-suss) head before fading away.</p>

<p>“Yes, Daxus?” Url acknowledged, shocked at the participation of the normally quiet student.</p>

<p>Daxus’s canine-like ears rotated, and his tail flicked. “I think there’s another card that captures the concept more completely.”</p>

<p>Url and the other students looked to Daxus. “Continue,” Url said.</p>

<p>Daxus reached a hand forward. In his visual field, the cards were not arranged in any discernible pattern. They looked as though someone had thrown them at his goggles at random, and they stuck there. His finger tapped a card, which became black with white text for everyone. The card read, “No identifiable, self-inflicted catastrophic mass event caused the extinction of First Earthers.”</p>

<p>“I think this card represents a higher-order concept that encompasses many of the others. Therefore, I suggest merging the card starting with ‘Human effects on climate…’ into this one. Then, we merge the group into that one. Per Deci’s observation, we should merge the ‘Climate change’ card into the group. That would be the first move,” Daxus finished.</p>

<p>Url smiled, and the feather-like sheeted layers atop his head fluttered in a wave. “Any dissent?”</p>

<p>Lafit spoke, “Not a dissent in the strictest sense, no. But I want to point out that a four-tier merge is rare for a reason.”</p>

<p>Daxus nodded. “I understand. That’s why I’m dividing it into multiple moves. Otherwise, it’d be more like a twelve-tier merge. But for this grouping, it was the smallest I could come to before speaking. If we prefer, we can start with a smaller move merging the card starting with ‘Human effects on climate…’ into the one starting with ‘No identifiable…,’ but that would not remove this group from consideration.”</p>

<p>Lafit slid her palms along the fabric of her Citizen Jeans. “As I said, not a dissent, just an observation.”</p>

<p>“Acknowledged,” Url said. “As such, the question remains: Does anyone dissent to this merge? Going once. Going twice. Done,” Url finished with a nod. “Make it so.”</p>

<p>A confirmation icon drifted from Daxus’s head.</p>

<p>The card reading “No identifiable, self-inflicted catastrophic mass event caused the extinction of First Earthers” absorbed the cards and groups and floated next to the card reading, “They left Earth.”</p>

<p>“Daxus, do you believe we can achieve your other proposals continuing as we were? Or, would you like to facilitate the discussion for a moment?”</p>

<p>“If it’s okay with you, Url, I’d like to try facilitating.”</p>

<p>Url nodded. “Please do,” he said, extending his hand palm up in Daxus’s direction as if presenting him with a gift.</p>

<p>“My next proposal is to change the articulation of ‘They left Earth’ to ‘First Earthers left Earth’ or ‘abandoned Earth.’”</p>

<p>“Why didn’t you propose that before the original merge?” Deci asked.</p>

<p>Daxus shrugged. “Didn’t think of it.”</p>

<p>“Why propose it now?”</p>

<p>“Historical relativism.”</p>

<p>The students tilted their heads to varying degrees in question, and almost in unison.</p>

<p>“In our current moment, we know who ‘They’ are. However, outside of this context and moment, ‘They’ could refer to anything, really,” Daxus added with a shrug.</p>

<p>Lafit said, “That’s a fair point. And the articulation doesn’t include any phrasing as to the subject at hand, which is the extinction of First Earthers.”</p>

<p>Url couldn’t contain himself. “Leaving Earth as an event also isn’t about extinction; it’s about exodus. So, I’d propose a change to the original prompt. Perhaps a better question would be: What reasons did we once consider and then dismiss for the disappearance of First Earthers from Earth? But I can iterate on articulating that. Thank you, Daxus, for revealing it.”</p>

<p>Daxus nodded, and an icon of a blushing face drifted from the top of his head.</p>

<p>Lafit’s voice almost squealed as she said, “I propose it be rephrased as ‘First Earthers did not abandon Earth.’”</p>

<p>Daxus added, “I consent. Any dissent?”</p>

<p>Daxus looked around the circle before resting on Url. A direct message flashed in front of Daxus’s eyes:</p>

<p><strong>Url:</strong> <em>Going once?</em></p>

<p>“Oh, right,” Daxus whispered. “Going once. Going twice. Done!”</p>

<p>Daxus looked at Url again. Url smiled and tilted his head down as if to say, “You asked to facilitate, so facilitate.”</p>

<p>With hesitation, Daxus looked to Lafit, “Make it so.” Despite being a statement, the inflection made it sound like a question.</p>

<p>Lafit made small, quick gestures that only made sense to her, updating the card.</p>

<p>“Next, I propose we duplicate and change the phrasing of the card starting with ‘No identifiable….’ The proposed phrasing would be ‘No identifiable, <em>external</em> catastrophic mass event caused the extinction of First Earthers.’ Any dissent?”</p>

<p>Deci said, “Not at this time, but I’m uncomfortable with where this might go next. Can you reassure us that rolling consent will continue?”</p>

<p>Daxus nodded. “Yes. And, don’t wait for me to request dissent before saying something,” Daxus said, looking each circle member in the eyes.</p>

<p>“Okay,” Daxus continued, “we have three higher-order concepts of why humans are no longer on Earth: Exodus, self-inflicted harm, and external harm. Most of the cards can be merged into one of these three,” he aid before quickly adding, “in my opinion.”</p>

<p>“If your card can be merged into one of these, do so. Otherwise, set it aside and mark it with a question mark.”</p>

<p>Url sat as still as he could to fade away from anyone’s consideration. </p>

<p>While he strongly believed in peer teaching, which meant he was a learner as much as a teacher, a separation of influence existed and had to be compensated for in order not to break the flow the others had found. </p>

<p>Url was a Fenghuang (fung-hwang), believed to be descended from flightless birds and the last species on Earth to interact with First Earthers. Even though over one thousand years separated Url from that overlapping period, people often considered the Fenghuang authorities on First Earthers.</p>

<p>Further, Url was deceptively older than the others in the group. It was common for Fourth Earthers, regardless of species, to live over one hundred years. Bearing that in mind, it was not unheard of for Fenghuang to live double that. This made them excellent historians, but also meant Url presented physically similar to and with the vibrancy of the others in the circle while being over twice their age.</p>

<p>As this rationale for fading into the background slid across his mind, Url had a memory come unbidden. It was at the end of Third Earth. Even though he was too young to fight in the war, no one was excluded from participating in it. He was barely old enough to form explicit memories, but he remembered being guarded and cared for by those not much older than Lafit and Daxus.</p>

<p>So much screaming. So much sound.</p>

<p>Babies and the pregnant stacked atop one another where the group now held class. The youth protected them, the adolescents protected the youth. The adults protected the adolescents, the middle-aged protected the adults, and the elderly protected the middle-aged.</p>

<p>So much death. So much pain.</p>

<p>“I think we got it!” Daxus exclaimed.</p>

<p>Url snapped out of his introspection in time to see the fading exclamation mark above Daxus’s head. He was slightly taken aback to see that almost everyone had left the seated circle.</p>

<p>The students were interacting with a shared projection. Lafit and Daxus were standing close to one another, facing Deci, who was also standing. Dea was, unsurprisingly, seated and physically still. The young Thales (thā-luss) almost disappeared from consideration, as she had during the entire session. The other three students were no longer in sight.</p>

<p>Url smiled. “I apologize. I wasn’t able to watch the journey. But, I’m excited to be shown where we’ve arrived.”</p>

<p>Daxus started, “I’m not sure if this is where you wanted us to go. We can revert all merges and…”</p>

<p>Url put up a hand, “Just show me. Iterate forward, not backward; it’s less painful that way.”</p>
